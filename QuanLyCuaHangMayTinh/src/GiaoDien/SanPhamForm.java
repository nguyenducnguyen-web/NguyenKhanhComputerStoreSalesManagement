    public ArrayList<SanPhamMayTinh> searchFn(String luaChon, String content) {
        ArrayList<SanPhamMayTinh> result = new ArrayList<>();
        TimKiemSanPham searchPr = new TimKiemSanPham();
        switch (luaChon) {
            case "Tất cả":
                result = searchPr.TimKiemTatCa(content);
                break;
            case "Mã máy":
                result = searchPr.TimKiemMaMay(content);
                break;
            case "Tên máy":
                result = searchPr.TimKiemTenMay(content);
                break;
            case "Số lượng":
                result = searchPr.TimKiemSoLuong(content);
                break;
            case "Đơn giá":
                result = searchPr.TimKiemDonGia(content);
                break;
            case "RAM":
                result = searchPr.TimKiemRam(content);
                break;
            case "CPU":
                result = searchPr.TimKiemCpu(content);
                break;
            case "Dung lượng":
                result = searchPr.TimKiemDungLuong(content);
                break;
            case "Card màn hình":
                result = searchPr.TimKiemCard(content);
                break;
            case "Xuất xứ":
                result = searchPr.TimKiemXuatXu(content);
                break;
            case "Đã xóa":
                result = searchPr.TimKiemDaXoa(content);
        }
        return result;
    }

