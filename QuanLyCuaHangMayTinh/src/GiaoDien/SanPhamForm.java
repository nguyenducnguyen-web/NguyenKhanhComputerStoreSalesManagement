    public void loadDataToTable() {
        try {
            SanPhamMayTinhDAO mtdao = new SanPhamMayTinhDAO();
            ArrayList<SanPhamMayTinh> armt = mtdao.selectAll();
            tblModel.setRowCount(0);
            for (SanPhamMayTinh i : armt) {
                if (i.getTrangThai() == 1) {
                    String loaimay;
                    if (SanPhamLapTopDAO.getInstance().isLaptop(i.getMaMay()) == true) {
                        loaimay = "Laptop";
                    } else {
                        loaimay = "PC/Case";
                    }
                    tblModel.addRow(new Object[]{
                        i.getMaMay(), i.getTenMay(), i.getSoLuong(), formatter.format(i.getGia()) + "đ", i.getTenCpu(), i.getRam(), i.getRom(), loaimay
                    });
                }
            }
        } catch (Exception e) {
        }
    }

