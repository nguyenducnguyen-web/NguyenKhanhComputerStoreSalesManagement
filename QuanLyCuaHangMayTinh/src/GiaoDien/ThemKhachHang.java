    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
        String maKh = txtMaKhachHang.getText().trim();
        String hoTen = txtHoTen.getText().trim();
        String sdtKh = txtPhone.getText().trim();
        String diaChiKh = txtDiaChi.getText().trim();

        // Kiểm tra các trường bắt buộc
        if (maKh.isEmpty() || hoTen.isEmpty() || sdtKh.isEmpty() || diaChiKh.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Kiểm tra định dạng số điện thoại (chỉ chứa số và phải có 10 hoặc 11 chữ số)
        if (!sdtKh.matches("\\d{10,11}")) {
            JOptionPane.showMessageDialog(this, "Số điện thoại phải chứa 10 hoặc 11 chữ số và chỉ được chứa số!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Kiểm tra mã khách hàng đã tồn tại chưa (dù đã tự động sinh mã, vẫn kiểm tra để đảm bảo)
        if (KhachHangDAO.getInstance().selectById(maKh) == null) { // Nếu mã chưa tồn tại
            KhachHang kh = new KhachHang(maKh, hoTen, sdtKh, diaChiKh);
            KhachHangDAO.getInstance().insert(kh);
            JOptionPane.showMessageDialog(this, "Thêm thành công!");
            parent.loadDataToTable(KhachHangDAO.getInstance().selectAll());
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Mã khách hàng đã tồn tại!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
        }
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Thêm không thành công!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        ex.printStackTrace();
    }
    }//GEN-LAST:event_jButton1ActionPerformed

