    public String createId(ArrayList<KhachHang> arr) {
        int id = arr.size() + 1; 
        String check = "";
        for (KhachHang kh : arr) {
            if (kh.getMaKhachHang().equals("KH" + String.format("%03d", id))) {
                check = kh.getMaKhachHang();
            }
        }
        while (check.length() != 0) {
            id++;
            check = "";
            for (KhachHang kh : arr) {
                if (kh.getMaKhachHang().equals("KH" + String.format("%03d", id))) {
                    check = kh.getMaKhachHang();
                }
            }
        }
        return "KH" + String.format("%03d", id);
    }

